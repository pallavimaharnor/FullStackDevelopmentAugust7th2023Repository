package com.gentech.programs;

public class VowelsUsingIfElse {
	public static void main(String[] args) {
		char ch=args[0].charAt(0);
		if(ch=='A') {
			System.out.println(ch+" is a vowel");
		}
		else if(ch=='E') {
			System.out.println(ch+" is a vowel");
		}
		else if(ch=='I') {
			System.out.println(ch+" is a vowel");
		}
		else if(ch=='O') {
			System.out.println(ch+" is a vowel");
		}
		else if(ch=='U') {
			System.out.println(ch+" is a vowel");
		}
		else
		{
			System.out.println(ch+" is a consonent");
		}
	}

}
