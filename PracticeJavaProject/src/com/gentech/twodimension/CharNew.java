package com.gentech.twodimension;

public class CharNew {

	public static void main(String[] args) {
		char  x[][]= {{'a','b','c'},{'d','e','f'},{'g','h','i'}};
		int j=1;
		for(int i=0;i<x.length;i++)
		{
			for( j=0;j<1;j++)
			{
				System.out.print(x[i][j]+" ");
			}
			System.out.println();
		}

	}

}
