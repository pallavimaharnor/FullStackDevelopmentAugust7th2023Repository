package com.gentech.twodimension;

public class CharArray {

	public static void main(String[] args) {
		char  x[][]= {{'a','b','c'},{'d','e','f'},{'g','h','i'}};
		for(int i=0;i<1;i++)
		{
			for(int j=0;j<x[i].length;j++)
			{
				System.out.print(x[i][j]+" ");
			}
			System.out.println();
		}
		
		

	}

}
